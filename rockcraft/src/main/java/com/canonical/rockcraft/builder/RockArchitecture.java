package com.canonical.rockcraft.builder;

/**
 * The list of supported Ubuntu architectures
 */
public enum RockArchitecture {
    /**
     * AMD64
     */
    amd64,
    /**
     * ARM64
     */
    arm64,
    /**
     * ARM hard float
     */
    armhf,
    /**
     * I386
     */
    i386,
    /**
     * PowerPC 64 EL
     */
    ppc64el,
    /**
     * RISCV
     */
    riscv64,
    /**
     * S390X
     */
    s390x
}
